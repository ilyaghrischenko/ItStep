pr = int(input("Введіть ціну приставки: "))
cil = int(input("Введіть кількість: "))
zn = int(input("Введіть відсоток знижки: "))

n = int(input("1 - загальна сума замовлення\n2 - вартість однієї приставки з урахуванням знижки: "))

if n == 1:
    total = pr * cil - (pr * (zn / 100))
    print(f"Загальна сума - {total}")
elif n == 2:
    total = pr - (pr * (zn / 100))
    print(f"Загальна сума - {total}")